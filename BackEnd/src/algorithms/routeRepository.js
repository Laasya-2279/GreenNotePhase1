/**
 * Get a route by source→dest key.  Checks donor routes first, then emergency
 * (depot) routes.
 * @param {string} sourceId - e.g. "H1" or "DEPOT"
 * @param {string} destId   - e.g. "H5"
 * @returns {Array<[number,number]>|null}
 */
function getRoute(sourceId, destId) {
    const key = `${sourceId}-${destId}`;
    return DONOR_ROUTES[key] || EMERGENCY_ROUTES[key] || null;
}

/**
 * Get all candidate emergency routes from the depot to each hospital.
 * @returns {Array<{id: string, name: string, route: Array<[number,number]>}>}
 */
function getAllEmergencyRoutes() {
    return Object.entries(HOSPITAL_NAMES).map(([id, name]) => ({
        id,
        name,
        route: EMERGENCY_ROUTES[`DEPOT-${id}`] || null,
    })).filter((h) => h.route !== null);
}

